package ru.gb.java2.lesson1;

public class Humen {
    private int distance;
    private int up;

    public Humen(int distance, int up) {
        this.distance = 100;
        this.up = 1;
    }

    public int getDistance() {
        return this.distance;
    }

    public int getUp() {
        return this.up;
    }
}
